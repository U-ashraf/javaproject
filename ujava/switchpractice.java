package ujava;

public class switchpractice {

	public static void main(String[] args) {
		String name="sabeen";
          switch(name)
          {
          case "usman":
        	  System.out.println("hi usman");
        	  break;
          case "aiza":
        	  System.out.println("hi aiza");
        	  break;
          case "ali":
        	  System.out.println("hi ali");
        	  break;
          case "aisha":
        	  System.out.println("hi aisha");
        	  break;
        	  default:
        		  System.out.println("no match");
        	  
          
          
          }
	}

}
